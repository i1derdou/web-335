# Define a function named `print_quote`
def print_quote():
    # Store the quote in a variable named `quote`
    quote = "‘It’s not the size of the dog in the fight, it’s the size of the fight in the dog.’"

    # Store the author's name in a variable named `author`
    author = "– Mark Twain"

    # Print the quote followed by the author's name
    print(f"{quote} {author}")

# Call the function to display the quote and author
print_quote()
